<?php

namespace Branches\Domain\Entity;

/**
 * Class AbstractEntity
 * @package Branches\Domain\Entity
 */
class AbstractEntity
{
    /**
     * @var int
     */
    private $id;

    /**
     * @return int
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * @param int $id
     * @return $this
     */
    public function setId($id)
    {
        $this->id = $id;
        return $this;
    }
}
